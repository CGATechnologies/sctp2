package org.cga.sctp.api.locations;

import com.fasterxml.jackson.annotation.JsonProperty;
import org.cga.sctp.location.LocationCode;

public class GeoLocationResponse implements LocationCode {
    @JsonProperty
    private Long id;

    @JsonProperty
    private Long code;

    @JsonProperty
    private String name;

    @JsonProperty
    private String locationType;

    public GeoLocationResponse() {
    }

    public GeoLocationResponse(Long id, Long code, String name, String locationType) {
        this.id = id;
        this.code = code;
        this.name = name;
        this.locationType = locationType;
    }

    @Override
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    @Override
    public Long getCode() {
        return code;
    }

    public void setCode(Long code) {
        this.code = code;
    }

    @Override
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String getLocationType() {
        return locationType;
    }

    public void setLocationType(String locationType) {
        this.locationType = locationType;
    }
}
