package org.cga.sctp.api.core.persistence;

/**
 * Indicates the status of a database record.
 */
public enum StatusCode {
    INACTIVE(0),
    ACTIVE(1),
    /**
     * Marks a record as deleted (soft deletion). This is good for keeping links to otherwise useful information.
     */
    DELETED(2);

    StatusCode(int code) {
        this.code = code;
    }

    /**
     * The actual status code. This is for tracking the code in case the ordering of the enum values changes.
     */
    public final int code;
}
