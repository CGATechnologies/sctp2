package org.cga.sctp.mis.auth;

import org.cga.sctp.validation.EmailAddress;
import org.cga.sctp.validation.UserName;
import org.hibernate.validator.constraints.Length;

import javax.validation.constraints.NotBlank;
import javax.validation.constraints.NotNull;

public class RegistrationForm {
    @EmailAddress
    private String email;

    @UserName
    private String username;

    @NotNull(message = "Captcha is required")
    @Length(min = 5, max = 5, message = "Invalid captcha")
    private String captcha;

    @NotBlank(message = "First name is required")
    @Length(min = 3, max = 30, message = "Must have {min}-{max} characters.")
    private String firstName;

    @NotBlank(message = "Last name is required")
    @Length(min = 3, max = 30, message = "Must have {min}-{max} characters.")
    private String lastName;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getCaptcha() {
        return captcha;
    }

    public void setCaptcha(String captcha) {
        this.captcha = captcha;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }
}
