package org.cga.sctp.targeting.criteria;

import javax.persistence.*;

@Entity
@Table(name = "filter_template_list_options")
public class FilterTemplateListOption {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private Long templateId;
    private String fieldValue;
    private String fieldText;

    @Enumerated(EnumType.STRING)
    private Operator operator;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getTemplateId() {
        return templateId;
    }

    public void setTemplateId(Long templateId) {
        this.templateId = templateId;
    }

    public String getFieldValue() {
        return fieldValue;
    }

    public void setFieldValue(String fieldValue) {
        this.fieldValue = fieldValue;
    }

    public String getFieldText() {
        return fieldText;
    }

    public void setFieldText(String fieldText) {
        this.fieldText = fieldText;
    }

    public Operator getOperator() {
        return operator;
    }

    public void setOperator(Operator operator) {
        this.operator = operator;
    }

    public String[] getBetweenOperatorValues() {
        if (operator != Operator.BETWEEN) {
            throw new IllegalStateException("Operator is not " + Operator.BETWEEN);
        }
        String[] parts = getFieldValue().split(",");
        assert parts.length == 2;
        return parts;
    }
}
