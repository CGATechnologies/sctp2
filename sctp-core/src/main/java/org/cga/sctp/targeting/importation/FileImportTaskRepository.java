package org.cga.sctp.targeting.importation;

import org.springframework.data.jpa.repository.Modifying;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.CrudRepository;
import org.springframework.data.repository.query.Param;
import org.springframework.stereotype.Repository;

import java.time.ZonedDateTime;

@Repository
interface FileImportTaskRepository extends CrudRepository<FileImportTask, Long> {

    @Modifying
    @Query(
            nativeQuery = true,
            value = "update file_import_tasks set status = :s, row_count = :rc, current_row = :cr, finished_at = :fa WHERE id = :id")
    void updateTask(
            @Param("id") Long id,
            @Param("s") String status,
            @Param("rc") Long rowCount,
            @Param("cr") Long currentRow,
            @Param("fa") ZonedDateTime finishedAt);
}
