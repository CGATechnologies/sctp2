package org.cga.sctp.messaging;

import org.simplejavamail.api.mailer.Mailer;
import org.simplejavamail.api.mailer.config.TransportStrategy;
import org.simplejavamail.mailer.MailerBuilder;
import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

@ConfigurationProperties(prefix = "simplejavamail")
@Configuration
public class EmailConfiguration {
    private SmtpConfig smtp;
    private TransportStrategy transportStrategy;

    private String fromName;
    private String fromAddress;

    public SmtpConfig getSmtp() {
        return smtp;
    }

    public void setSmtp(SmtpConfig smtp) {
        this.smtp = smtp;
    }

    public TransportStrategy getTransportStrategy() {
        return transportStrategy;
    }

    public void setTransportStrategy(TransportStrategy transportStrategy) {
        this.transportStrategy = transportStrategy;
    }

    public String getFromName() {
        return fromName;
    }

    public void setFromName(String fromName) {
        this.fromName = fromName;
    }

    public String getFromAddress() {
        return fromAddress;
    }

    public void setFromAddress(String fromAddress) {
        this.fromAddress = fromAddress;
    }

    @Bean
    Mailer mailer() {
        return MailerBuilder
                .withSMTPServer(smtp.getHost(), smtp.getPort(), smtp.getUsername(), smtp.getPassword())
                .buildMailer();
    }
}
